package com.Basic.Domain;

public class UserVO {
	private int unum;
    private String uid;
    private String upass;
    private String uname;
    private String umail01;
    private String umail02;
    
	public int getUnum() {
		return unum;
	}
	public void setUnum(int unum) {
		this.unum = unum;
	}
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	public String getUpass() {
		return upass;
	}
	public void setUpass(String upass) {
		this.upass = upass;
	}
	public String getUname() {
		return uname;
	}
	public void setUname(String uname) {
		this.uname = uname;
	}
	public String getUmail01() {
		return umail01;
	}
	public void setUmail01(String umail01) {
		this.umail01 = umail01;
	}
	public String getUmail02() {
		return umail02;
	}
	public void setUmail02(String umail02) {
		this.umail02 = umail02;
	}

}
